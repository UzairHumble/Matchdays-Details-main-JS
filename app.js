function details(el) {
  getModalDiv.style.display = 'flex';

  var detailsBody = document.createElement('div');
  detailsBody.setAttribute('id', 'details-body');
  detailsDiv.appendChild(detailsBody);
  //=============================================
  var tbl = document.createElement('table');
  var trHeadings = document.createElement('tr');
  trHeadings.setAttribute('id', 'table-headings');
  var tableHeadings = [
    'date',
    'team 1',
    'team 2',
    'team-1 score',
    'team-2 score',
  ];
  for (var i = 0; i < 5; i++) {
    var tdOfHead = document.createElement('td');
    tdOfHead.appendChild(
      document.createTextNode(tableHeadings[i].toUpperCase())
    );
    trHeadings.appendChild(tdOfHead);
  }
  tbl.appendChild(trHeadings);
  detailsBody.appendChild(tbl);
  //====================================================
  for (var key2 in rounds[el].matches) {
    //loop on selected round with the help of(el)

    var selectedRound = rounds[el].matches[key2];
    //console.log(selectedRound)

    var trProperties = document.createElement('tr');
    tbl.appendChild(trProperties);

    for (var key3 in selectedRound) {
      if (typeof selectedRound[key3] === 'object') {
        var tdd1 = document.createElement('td');
        tdd1.appendChild(document.createTextNode(selectedRound.score.ft[0]));
        trProperties.appendChild(tdd1);

        var tdd2 = document.createElement('td');
        tdd2.appendChild(document.createTextNode(selectedRound.score.ft[1]));
        trProperties.appendChild(tdd2);
      } else {
        var tdd = document.createElement('td');
        tdd.appendChild(document.createTextNode(selectedRound[key3]));
        trProperties.appendChild(tdd);
      }
    }
  }
}
function closeTab() {
  getModalDiv.style.display = 'none';

  var a = document.getElementById('detail-box');
  var childToRemove = a.childNodes;
  for (var e = 1; e < childToRemove.length; e++) {
    //remove previous data of modal, if it exist
    a.removeChild(childToRemove[e]);
  }
}
